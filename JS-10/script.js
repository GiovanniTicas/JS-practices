// LOOPS: WHILE

let myNumber = 50

while (myNumber < 50) {
    console.log(myNumber)
    myNumber++
}

// LOOPS: DO WHILE

do {
    console.log(myNumber)
} while (myNumber < 50)

// LOOPS: FOR

// let arr  = ['a','b','c']
let name = "Giovanni"
for (let i = 0; i < name.length; i++) {

    console.log(name[i] )
}